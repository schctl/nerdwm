//! Predefined input events.

use serde::{Deserialize, Serialize};

/// Modifier masks.
#[repr(u32)]
#[non_exhaustive]
#[derive(Debug, Deserialize, Serialize, Clone, Copy)]
pub enum ModMask {
    Shift = 0x01,
    Lock = 0x02,
    Control = 0x04,
    /// Usually `Alt`.
    Mod1 = 0x08,
    /// Usually `Num Lock`.
    Mod2 = 0x10,
    /// Usually `Scroll Lock`.
    Mod3 = 0x20,
    /// Usually `Super`.
    Mod4 = 0x40,
    Mod5 = 0x80,
}

/// Mouse buttons.
#[repr(u8)]
#[non_exhaustive]
#[derive(Debug, Deserialize, Serialize, Clone, Copy)]
pub enum Button {
    /// Usually the Left button.
    Button1 = 0x01,
    /// Usually the Middle button.
    Button2 = 0x02,
    /// Usually the Right button.
    Button3 = 0x03,
    Button4 = 0x04,
    Button5 = 0x05,
}

